impl Script {
    fn words(self) -> &'static [&'static str] {
        match self {
            Latin => LATIN_WORDS,
            LatinExt => LATIN_EXT_WORDS,
            Cyrillic => CYRILLIC_WORDS,
            Greek => GREEK_WORDS,
            Japanese => JAPANESE_WORDS,
            Chinese => CHINESE_WORDS,
            Thai => THAI_WORDS,
            Hindi => HINDI_WORDS,
        }
    }

    /// Japanese, Chinese and Thai are written with no space between words.
    fn separator(self) -> &'static str {
        match self {
            Japanese | Chinese | Thai => "",
            _ => " ",
        }
    }

    fn sentence(self, rng: &mut Rng) -> String {
        let words = self.words();
        let count = 3 + rng.below(5);

        (0..count)
            .map(|_| words[rng.below(words.len())])
            .collect::<Vec<_>>()
            .join(self.separator())
    }
}

/// Written out here rather than taken from a crate. A generator crate is
/// free to change its algorithm in a minor release, and a `cargo update`
/// would then silently rewrite every sentence and every recorded probe.
struct Rng(u64);

impl Default for Rng {
    fn default() -> Self {
        Self(0x2545_F491_4F6C_DD1D)
    }
}

impl Rng {
    fn next(&mut self) -> u64 {
        self.0 ^= self.0 << 13;
        self.0 ^= self.0 >> 7;
        self.0 ^= self.0 << 17;
        self.0
    }

    fn below(&mut self, bound: usize) -> usize {
        let bound = u64::try_from(bound).unwrap();
        usize::try_from(self.next() % bound).unwrap()
    }
}

fn font_by_name(name: &str) -> Weak<Font> {
    match name {
        "helvetica" => Font::helvetica(),
        "san_francisco" => Font::san_francisco(),
        "roboto" => Font::roboto(),
        file => Font::get(file),
    }
}

#[view]
struct LabelStress {
    rng:    Rng,
    labels: Vec<Weak<Label>>,
}

impl LabelStress {
    fn randomize(mut self: Weak<Self>) {
        for (label, (_, script)) in self.labels.clone().into_iter().zip(SLOTS) {
            label.set_text(script.sentence(&mut self.rng));
        }
    }
}

impl Setup for LabelStress {
    fn setup(mut self: Weak<Self>) {
        let canvas_width: f32 = CANVAS.0.lossy_convert();
        let canvas_height: f32 = CANVAS.1.lossy_convert();
        let columns: f32 = COLUMNS.lossy_convert();
        let rows: f32 = ROWS.lossy_convert();

        let cell_width = canvas_width / columns;
        let cell_height = canvas_height / rows;

        for (index, (font, _)) in SLOTS.into_iter().enumerate() {
            let column: f32 = (index % COLUMNS).lossy_convert();
            let row: f32 = (index / COLUMNS).lossy_convert();

            let x = column * cell_width;
            let y = row * cell_height;

            let label = self.add_view::<Label>();
            label
                .set_frame((x, y, cell_width, cell_height))
                .set_text_size(TEXT_SIZE)
                .set_alignment(TextAlignment::Left)
                .set_font(font_by_name(font));

            self.labels.push(label);
        }

        self.randomize();
    }
}

impl ViewTest for LabelStress {
    fn canvas() -> (u32, u32) {
        CANVAS
    }

    fn perform_test(view: Weak<Self>) -> Result<()> {
        set_record_probe_count(512);

        for iteration in 1..=ITERATIONS {
            from_main(move || view.randomize());
            wait_for_next_frame();

            match iteration {
                20 => check_colors(COLORS_20)?,
                50 => check_colors(COLORS_50)?,
                ITERATIONS => check_colors(COLORS_80)?,
                _ => {}
            }
        }

        Ok(())
    }
}

