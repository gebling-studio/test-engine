const COLUMNS: usize = 2;

/// A row is `CANVAS.1 / ROWS` = 50 pixels. That is the floor, not a taste:
/// a label bound shorter than the font's ascent to descent crops the glyph
/// tops and bottoms. At 32 pixels the tallest font here, Monoton, inks 49.8
/// pixels, Noto Thai 48.4 and `DotGothic16` 46.3. 25 rows of 40 cut all three.
const ROWS: usize = 20;

const LABELS: usize = COLUMNS * ROWS;

/// 640 wide is the iPhone 5S screen. 1000 tall, not the phone's 1136,
/// because the headless surface is `App::initial_size`, which is 1200 by
/// 1000. A taller canvas renders nothing below 1000 on desktop and the
/// probe recorder silently clips to the screenshot, so those rows would
/// look tested and never run.
const CANVAS: (u32, u32) = (640, 1000);

/// Below 32 a glyph stem is thinner than 3 pixels, and the probe recorder
/// only pins a pixel whose 3x3 neighborhood is uniform. Smaller text would
/// record a block of background and check nothing.
const TEXT_SIZE: u32 = 32;

const ITERATIONS: usize = 80;

