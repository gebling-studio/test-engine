fn font_by_name(name: &str) -> Weak<Font> {
    match name {
        "helvetica" => Font::helvetica(),
        "san_francisco" => Font::san_francisco(),
        "roboto" => Font::roboto(),
        file => Font::get(file),
    }
}

#[view_test]
struct FontZoo {
    labels: Vec<Weak<Label>>,
}

impl FontZoo {
    fn set_page(self: Weak<Self>, page: usize) {
        for (label, (font, text)) in self.labels.clone().into_iter().zip(PAGES[page]) {
            label.set_font(font_by_name(font)).set_text(text);
        }
    }
}

impl Setup for FontZoo {
    fn setup(mut self: Weak<Self>) {
        for row in 0..ROWS {
            let y: f32 = 6.0 + 59.0 * row.lossy_convert();
            let label = self.add_view::<Label>();
            label
                .set_frame((6.0, y, 588.0, 56.0))
                .set_text_size(40)
                .set_alignment(TextAlignment::Left);
            self.labels.push(label);
        }

        self.set_page(0);
    }
}

